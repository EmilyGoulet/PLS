if ( ! function_exists( 'business_point_reset_colors' ) ) :

    function business_point_reset_colors( $data ) {

            $reset_colors = business_point_get_option( 'reset_colors' );

            if ( true == $reset_colors ) {

                $options = business_point_get_options();

                $options['reset_colors'] 		= false;

                $color_settings = business_point_get_default_color_options();

                if ( ! empty( $color_settings ) ) {

                    foreach ( $color_settings as $key => $val ) {

                        $options[ $key ] = $val;

                    }
                }

                update_option( 'theme_mods_business-point-plus', $options );

            }

    }

endif;

add_action( 'customize_save_after', 'business_point_reset_colors' );

